public class TypeDataNumber
{   
	public static void main (String[] args)
	{
        byte typeByte = 100;
        short typeShort = 1_000;
        int typeInt = 1_000_000;
        long typeLong = 1_000_000L;

		System.out.println(typeByte);
		System.out.println(typeShort);
		System.out.println(typeInt);
		System.out.println(typeLong);

        float typeFloat = -1.8F;
        double typeDouble = 1.000000000000000000000000000009F;
        
		System.out.println(typeFloat);
		System.out.println(typeDouble);


	}
}

